function H = secondaxis2(Pplot,shallow)
    %Add a second axis with monthly dates below original x axis
    %======================================================================
    %Position axis
    abc=get(Pplot, 'Position');
    abcd=abc-[0 0.035 0 0];
    abcde=abc-[0 0.031 0 0];
    abcdef=abc-[0 0.043 0 0];
    %======================================================================
    %Set dates
    inoutlim = [datenum('5-1-2014') datenum('6-1-2016')];
    %======================================================================
    %Run process depending on plot type
    if shallow == 0
        axes1 = axes('Position',abcd,...
            'Color', 'none', 'Ycolor', 'none', ...
            'Xlim', inoutlim, ...
            'XTick', [datenum('06-01-2014') datenum('01-01-2015') ...
            datenum('06-01-2015') datenum('01-01-2016') datenum('06-01-2016')],...
            'XTickLabel', {'2014','','2015','','2016'}, 'FontSize', 14);
    end
    if shallow == 1
        axes1 = axes('Position',abcde,...
            'Color', 'none', 'Ycolor', 'none', ...
            'Xlim', inoutlim, ...
            'XTick', [datenum('06-01-2014') datenum('01-01-2015') ...
            datenum('06-01-2015') datenum('01-01-2016') datenum('06-01-2016')],...
            'XTickLabel', {'2014','','2015','','2016'}, 'FontSize', 14,...
            'TickLength',[0.003 0.003]);
    end
    if shallow == 2
        axes1 = axes('Position',abcdef,...
            'Color', 'none', 'Ycolor', 'none', ...
            'Xlim', inoutlim, ...
            'XTick', [datenum('06-01-2014') datenum('01-01-2015') ...
            datenum('06-01-2015') datenum('01-01-2016') datenum('06-01-2016')],...
            'XTickLabel', {'2014','','2015','','2016'}, 'FontSize', 14);
    end
end